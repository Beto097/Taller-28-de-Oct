package co.com.choucair.certification.ProyectoBase.model;

public class MedilappData {
    private String strUser;
    private String strPassword;
    private String strInicio;
    private String strError;
    private String strTelefono;
    private String strCorreo;
    private String strExitoso;

    public String getStrUser() {
        return strUser;
    }

    public void setStrUser(String strUser) {
        this.strUser = strUser;
    }

    public String getStrPassword() {
        return strPassword;
    }

    public void setStrPassword(String strPassword) {
        this.strPassword = strPassword;
    }

    public String getStrInicio() {
        return strInicio;
    }

    public void setStrInicio(String strInicio) {
        this.strInicio = strInicio;
    }

    public String getStrError() {
        return strError;
    }

    public void setStrError(String strError) {
        this.strError = strError;
    }

    public String getStrTelefono() {
        return strTelefono;
    }

    public void setStrTelefono(String strTelefono) {
        this.strTelefono = strTelefono;
    }

    public String getStrCorreo() {
        return strCorreo;
    }

    public void setStrCorreo(String strCorreo) {
        this.strCorreo = strCorreo;
    }

    public String getStrExitoso() {
        return strExitoso;
    }

    public void setStrExitoso(String strExitoso) {
        this.strExitoso = strExitoso;
    }

}
